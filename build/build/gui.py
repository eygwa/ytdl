def downloadVideo(url, type):
#todo
    try: yt = YouTube(url)
    except: 
        print('invalid link')
        return "invalid link"
    
    if type=='audio': 
        streams = yt.streams.filter(only_audio=True).order_by('abr').desc()
    else: 
        streams = yt.streams.filter(progressive=True).order_by('resolution').desc()

    qual=streams[0].download('outputs') # get highest quality

    print(yt.title+' downloaded')

    if type=='audio':
        try: subprocess.run('ffmpeg -y -i outputs/"'+yt.title+'.webm" outputs/"'+yt.title+'.mp3"', shell=True)
        except:
            return 'file error, perhaps the mp3 aready exists'
        os.remove('outputs/'+yt.title+'.webm', dir_fd = None)
        
    return "downloaded " + yt.title

